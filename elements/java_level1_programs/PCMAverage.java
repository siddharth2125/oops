class PCMAverage {
    public static void main(String[] args) {
        int mathsMarks = 94;
        int physicsMarks = 95;
        int chemistryMarks = 96;
        String name = "Sam";

        double averageMarks = (mathsMarks + physicsMarks + chemistryMarks) / 3.0;

        System.out.println(name + "'s average mark in PCM is " + averageMarks);
    }
}
